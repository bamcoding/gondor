package net.gondor;

/**
 * (+)(-)(*)(/) 1. main 메소드에서 수행하세요.
 *
 * 
 */
public class Main {
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 50;
		System.out.printf("%d + %d = %d\n",num1 ,num2 ,num1+num2);
		System.out.printf("%d - %d = %d\n",num1 ,num2 ,num1-num2);
		System.out.printf("%d * %d = %d\n",num1 ,num2 ,num1*num2);
		System.out.printf("%d / %d = %.2f\n",num1 ,num2 ,(float)num1/num2);
	}
}
